export const beginnerCategories: Category[] = [animals, fruits, vegetables, vehicles, bodyParts, sweets];
export const intermediateCategories: Category[] = [prefectures, sports, fish, insects, flowers];
export const advancedCategories: Category[] = [countries, japaneseCities, cooking];
export const expertCategories: Category[] = [capitals, historicalFigures, musicalInstruments];

export function getCategoriesForDifficulty(difficulty: string): Category[] {
  switch (difficulty) {
    case '初級': return beginnerCategories;
    case '中級': return intermediateCategories;
    case '上級': return advancedCategories;
    case '鬼': return expertCategories;
    default: return beginnerCategories;
  }
}

export function getRandomCategory(difficulty: string): Category {
  const cats = getCategoriesForDifficulty(difficulty);
  return cats[Math.floor(Math.random() * cats.length)];
}
